def get_system_intro(question: str) -> str | None:
    """
    判断用户是否在问“你是谁 / 能做什么 / 区别”
    如果是 → 返回固定回答
    否则 → 返回 None
    """

    q = question.lower()

    keywords_identity = ["你是谁", "你是什么", "who are you"]
    keywords_ability = ["你能做什么", "有什么功能", "能干嘛"]
    keywords_compare = ["chatgpt", "区别", "有什么不同"]

    # ===== 1. 身份 =====
    if any(k in q for k in keywords_identity):
        return """
## 🤖 我是谁？

我是一个基于 **RAG（检索增强生成）技术** 的智能问答助手。

与传统大模型不同，我的回答：
- 📚 优先基于你的私有知识库
- 🔍 可展示参考来源、原文片段、相似度
- 🧠 减少“胡编乱造”（幻觉）

---

## 🚀 如何使用？

1. 在左侧上传你的文档（PDF / Word / TXT）
2. 点击「更新知识库」
3. 直接提问即可

💡 示例：
- 这个方案的核心架构是什么？
- 总结一下这个文档
"""

    # ===== 2. 能力 =====
    if any(k in q for k in keywords_ability):
        return """
## 🧠 我可以做什么？

### 📄 1. 基于知识库问答（核心能力）
- 从你的文档中找答案
- 展示引用来源（可追溯）

### 🌐 2. 联网搜索（可选）
- 当知识库没有内容时补充信息

### ⚙️ 3. 可调参数
- Top-K（召回数量）
- 相似度阈值（控制准确性）

---

## 📌 适用场景

- 企业知识库问答
- 面试项目展示
- 文档总结与分析
"""

    # ===== 3. 区别 =====
    if any(k in q for k in keywords_compare):
        return """
## ⚔️ 我 vs ChatGPT

| 对比项 | 我（RAG系统） | ChatGPT |
|--------|-------------|--------|
| 数据来源 | 私有知识库 + 可联网 | 训练数据 |
| 可解释性 | ✅ 展示来源 | ❌ 黑盒 |
| 幻觉控制 | ✅ 强 | ❌ 较弱 |
| 企业适用 | ✅ 非常适合 | ⚠️ 有风险 |

---

## 💡 核心区别一句话：

👉 我是“带证据回答”的AI  
👉 ChatGPT是“基于记忆回答”的AI
"""

    return None